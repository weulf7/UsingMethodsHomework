package org.example;

public class Operations {

    int x;
    int y;


    public int operation (int x,int y ){

        int sumOperation = x + y;
        return sumOperation;

    }
    public int multipliedOperation(int x,int y){

        int multipliedOp = x * y;
        return multipliedOp;
    }

    public int minusOperation(int x ,int y){

        int minusOp = x - y ;
        return minusOp;
    }
    public double divideOperations(double x, double y){
        double divideOp = x / y ;
        return divideOp;

    }

    }

